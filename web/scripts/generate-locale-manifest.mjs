#!/usr/bin/env node
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const DEFAULT_LOCALE = "en";
const __dirname = dirname(fileURLToPath(import.meta.url));
const webRoot = join(__dirname, "..");
const messagesDir = join(webRoot, "messages");
const outputPath = join(messagesDir, "manifest.ts");

function safeIdent(code) {
  return `bundle_${code.replace(/[^A-Za-z0-9_]/g, "_")}`;
}

function readBundle(code) {
  return JSON.parse(readFileSync(join(messagesDir, `${code}.json`), "utf8"));
}

function localeMeta(code) {
  const bundle = readBundle(code);
  const attunement = bundle._attunement && typeof bundle._attunement === "object"
    ? bundle._attunement
    : {};
  const name = attunement.name || attunement.languageName || code;
  const nativeName = attunement.nativeName || attunement.native_name || name;
  return { code, name, nativeName };
}

const files = readdirSync(messagesDir)
  .filter((file) => /^[A-Za-z][A-Za-z0-9-]*\.json$/.test(file))
  .sort();

if (!files.includes(`${DEFAULT_LOCALE}.json`)) {
  throw new Error(`web/messages/${DEFAULT_LOCALE}.json is required`);
}

const discovered = files.map((file) => basename(file, ".json"));
const codes = [
  DEFAULT_LOCALE,
  ...discovered.filter((code) => code !== DEFAULT_LOCALE).sort(),
];

const imports = codes.map((code) => `import ${safeIdent(code)} from "./${code}.json";`).join("\n");
const locales = codes.map(localeMeta);
const bundles = codes.map((code) => `  ${JSON.stringify(code)}: ${safeIdent(code)} as MessageTree,`);

const source = `// Generated by web/scripts/generate-locale-manifest.mjs. Do not edit by hand.
${imports}

export const DEFAULT_LOCALE = ${JSON.stringify(DEFAULT_LOCALE)} as const;

export const LOCALES = ${JSON.stringify(locales, null, 2)} as const;

export type LocaleCode = (typeof LOCALES)[number]["code"];
export type Locale = (typeof LOCALES)[number];
export type MessageTree = Record<string, unknown>;

export const BUNDLES: Record<LocaleCode, MessageTree> = {
${bundles.join("\n")}
};
`;

writeFileSync(outputPath, source);
console.log(`generated messages/manifest.ts for ${codes.join(", ")}`);
